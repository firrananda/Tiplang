public class PelangganRev {
    private int id;
    private String nomor_batd, tanggal_batd, nomor_pelanggan, nama, jalan;

    public int getId() {
        return id;
    }

    public String getNomor_batd() {
        return nomor_batd;
    }

    public String getTanggal_batd() {
        return tanggal_batd;
    }

    public String getNomor_pelanggan() {
        return nomor_pelanggan;
    }

    public String getNama() {
        return nama;
    }

    public String getJalan() {
        return jalan;
    }
}
